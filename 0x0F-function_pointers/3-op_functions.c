#include "3-calc.h"

/**
 * op_add - function that calculat sum of a and b
 * @a:number
 * @b:number
 * Return: sum of a and b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that calculat difference of a and b
 * @a:number
 * @b:number
 * Return: difference of a and b
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that calculat product of a and b
 * @a:number
 * @b:number
 * Return: product of a and b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that calculat the div of a and b
 * @a:number
 * @b:number
 * Return: result of the division of a and b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that calculat the remainder of the division  of a and b
 * @a:number
 * @b:number
 * Return: the remainder of the division  of a and b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
